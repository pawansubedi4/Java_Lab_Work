class Escape {
    public static void main(String[] args){
        System.out.println("For \\n use : new \n nepal");
        System.out.println("For \\t use :  new\tnepal");
        System.out.println("For \\b use : ABCD\babc");
        System.out.println("For \\r use : \nBpple\rA");
        System.out.println("Line before form feed\fLine after form feed");
        System.out.println("For \\\" use : \"new nepal\"");
        System.out.println("For \\\' use : \'character\'");

        
    }
}